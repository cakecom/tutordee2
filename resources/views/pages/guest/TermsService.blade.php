                        <h2>8. การระงับการใช้บริการ</h2>
                        <p>
                            หากผู้ให้บริการตรวจพบข้อมูลสมาชิกที่ผิดข้อตกลงตามที่ได้กล่าวเป็นข้อๆดังต่อไปนี้
                            จะดำเนินการงดการให้บริการทันที โดยไม่มีการคืนค่าบริการใดๆ ทั้งสิ้น
                            และไม่จำเป็นต้องแจ้งให้ทราบล่วงหน้า
                            ทั้งนี้ผู้ให้บริการขอสงวนสิทธิ์ในการพิจารณาตรวจสอบข้อมูลผิดตามข้อตกลงหรือไม่
                            หากมีข้อโต้แย้งใดๆ ให้ถือเอาคำตัดสินของผู้ให้บริการเป็นที่สิ้นสุด
                        </p>
                        <br>
                        8.1 ผิดกฎหมายหรือขัดต่อศีลธรรมอันดีของไทย เช่น การละเมิดลิขสิทธิ์ของผู้อื่น การข่มขู่ เว็บไซต์ที่เกี่ยวกับสื่อลามก หรือเนื้อหาส่อไปทางลามก เซ็กส์ ขายบริการทางเพศ รวมทั้งที่มีลิงก์ไปยังเว็บไซต์ที่เกี่ยวกับสื่อลามก
                        <br>
                        8.2 เกี่ยวข้องกับธุรกิจเครือข่าย และผลิตภัณฑ์ที่จำหน่ายโดยมีความสัมพันธ์ตามค่าคอมมิชชั่น ซึ่งรวมทั้งหน้ารายการที่ทำขึ้นมาจากโฆษณาเป็นหลัก
                        <br>
                        8.3 พบทุจริต ฉ้อโกง ในการซื้อ-ขายบริการ ไม่ว่ากรณีใดๆ ทั้งสิ้น (ทั้งในอดีตและปัจจุบัน) ทั้งนี้ผู้ให้บริการขอสงวนสิทธิ์ในการตัดสินใจว่าสิ่งใดถือว่าเป็นการทุจริต
                        <br>
                        8.4 เกี่ยวกับ Hacker , Warez , Crack, เผยแพร่ไวรัส หนอนอินเตอร์เน็ต โทร์จัน หรืออื่นๆ ที่เกี่ยวข้องกับซอฟท์แวร์ผิดกฏหมาย
                        <br>
                        8.5 หลอกลวงผู้อื่นด้วยการแสดงข้อความอันเป็นเท็จ โฆษณาเกินจริง หรือปกปิดข้อความจริงซึ่งควรบอกให้แจ้ง
                        <br>
                        8.6 ใช้งานที่ส่งผลกระทบกระเทือนต่อระบบและทรัพยากรของระบบ อาทิเช่น การใช้งาน CPU เกินขอบเขต การใช้งาน Email เกินขอบเขต การเจาะระบบ (Hacking) การใช้งานโปรแกรม IRC Bots หรือการใช้งานโปรแกรมใด ๆ ที่ส่งผลกระทบต่อการทำงานของระบบ และผู้ให้บริการขอสงวนสิทธิ์ในการพิจารณาว่า การใช้งานใดเป็นการใช้งานอย่างเกินขอบเขต
