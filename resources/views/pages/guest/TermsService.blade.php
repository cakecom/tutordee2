                            และ ผู้ซื้อบริการ ไม่มีสิทธิ์เรียกค่าบริการคืน ไม่ว่าบางส่วน หรือ ทั้งหมดได้
                            เว้นแต่ทางผู้ให้บริการจะเป็นผู้ยกเลิกบริการเอง โดยจะคืนค่าบริการตามสมควรให้เท่านั้น
                            และผู้ซื้อบริการจะไม่สามารถเรียกร้องค่าเสียหายอื่น ๆ ที่เกิดขึ้นกับการใช้บริการนั้นๆ
                            นอกเหนือจากค่าบริการที่ผู้ซื้อบริการได้ชำระกับผู้ให้บริการแล้วเท่านั้น
                            <br>
                            7.2 กรณีมีการละเมิดข้อตกลงการใช้งานผู้ให้บริการจะแจ้งเตือน
                            และจะหยุดให้บริการหากผู้ซื้อบริการไม่ทำการแก้ไขให้ถูกต้อง และผู้ซื้อบริการจะไม่สามารถ
                            เรียกร้องสิทธิ์ในการคืนเงินได้ และหากเป็นการกระทำที่ส่งผลกระทบต่อ ระบบ เครือข่าย
                            และชื่อเสียงของผู้ให้บริการ ผู้ซื้อบริการจะต้องรับผิดชอบความเสียหายที่เกิดขึ้นทั้งหมด

                        </p>
